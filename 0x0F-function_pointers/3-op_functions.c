#include <stdio.h>
/**
  * op_add - function add
  *@a:value
  *@b: value
  *Return: value
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - function sub
  *@a: value
  *@b: value
  *Return: value
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - function multiply
  *@a: value
  *@b: value
  *Return: value
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - function div
  *@a: value
  *@b: value
  *Return: value
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - function mod
  *@a: value
  *@b: value
  *Return: value
  */
int op_mod(int a, int b)
{
	return (a % b);
}
